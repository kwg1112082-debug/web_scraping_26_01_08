import requests

# ---------------------------------------------------------
# [초보자 가이드] 파이썬으로 웹 정보 가져오기 (HTTP 요청)
# ---------------------------------------------------------
# 우리가 브라우저(크롬 등)에 주소를 입력하면, 브라우저는 해당 서버에
# "정보 좀 주세요!"라고 요청을 보냅니다. 이를 'HTTP 요청'이라고 합니다.
# 파이썬에서는 'requests'라는 도구(라이브러리)를 사용해서 이 과정을 대신할 수 있습니다.

# 1. 정보를 가져올 주소 설정하기 (구글 페이지를 예로 들어볼게요)
url = "https://www.google.com"

print(f"[{url}] 에 접속을 시도합니다...")

# 2. 서버에 'GET' 요청 보내기
# get()은 데이터를 가져올 때 가장 많이 사용하는 명령어입니다.
response = requests.get(url)

# 3. 결과 확인하기 (상태 코드)
# 서버가 요청을 잘 받았는지 확인해야 합니다.
# 200: 성공! (OK)
# 404: 페이지를 찾을 수 없음 (Not Found)
# 500: 서버 내부 오류
print(f"응답 상태 코드: {response.status_code}")

if response.status_code == 200:
    print("성공적으로 정보를 가져왔습니다! \n")
    
    # 4. 가져온 내용 살펴보기 (일부만 출력)
    # response.text는 웹사이트의 실제 HTML 코드를 담고 있습니다.
    print("--- 가져온 HTML 내용 (앞부분 200자) ---")
    print(response.text[:200])
    
    # 5. 간단한 팁
    # response.encoding을 확인하거나 설정해서 글자 깨짐을 방지할 수 있습니다.
    print(f"\n인코딩(글자 형식): {response.encoding}")
else:
    print("오류가 발생했습니다. 주소를 다시 확인해주세요.")

